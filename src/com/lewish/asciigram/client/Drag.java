package com.lewish.asciigram.client;

public class Drag {
	private Cell start;
	private Cell finish;

	public void setStart(Cell start) {
		this.start = start;
	}

	public Cell getStart() {
		return start;
	}

	public void setFinish(Cell finish) {
		this.finish = finish;
	}

	public Cell getFinish() {
		return finish;
	}
}
